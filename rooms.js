var RoomStorage = new Class({
	 rooms: []
	,row: 0
	,col: 0
	,name: ''
	,hasCompass: false
	,hasMap: false
	,initialize: function(defRow, defCol, rooms, props) {
		this.row = defRow;
		this.col = defCol;
		this.addRooms(rooms);
		if(props && props.name)
			this.name = props.name;
	}
	,addRoom: function(room) {
		if(!this.rooms[room.row]) this.rooms[room.row] = [];
		this.rooms[room.row][room.col] = room;
	}
	,addRooms: function(rooms) {
		Array.each(rooms, function(room){
			this.addRoom(room);
		}, this);
	}
	,getRoom: function(row,col) {
		if(this.rooms[row] && this.rooms[row][col])
			return this.rooms[row][col];
		return null;
	}
	,getCurrentRoom: function() {
		return this.rooms[this.row][this.col];
	}
	,switchRoom: function(row, col) {
		if(this.exists(row,col)) {
			rooms.getCurrentRoom().fireEvent('leave');
			this.row = row;
			this.col = col;
			cr = this.getRoom(row,col);
			cr.fireEvent('enter');
			return cr;
		}
		return false;
	}
	,exists: function(row,col) {
		return (this.rooms[row] && this.rooms[row][col]);
	}
});

var Room = new Class({
	Implements: Events
	,roomWidth: 16
	,roomHeight: 11
	,initialized: false
	,background: null
	,triforceRoom: false
	,row: null
	,col: null
	,tiles: []
	,tileset: 0
	,MOBs: []
	,bgRect: null
	,initialize: function(params) {
		if(params.row != undefined) this.row = params.row;
		if(params.col != undefined) this.col = params.col;
		if(params.tiles) this.tileData = params.tiles;
		if(params.background) this.background = params.background;
		if(params.scriptedEvent) this.scriptedEvent = params.scriptedEvent;
		if(params.tileset) this.tileset = params.tileset;
		if(params.triforceRoom) this.triforceRoom = true;
		
		if(params.bgRect) this.bgRect = params.bgRect;
		
		for(var i=0; i < this.roomHeight; i++) {
			this.tiles[i] = [];
			for(var j=0; j < this.roomWidth; j++) {
				if(!params.tiles[i]) sprite = null;
				else sprite = (params.tiles[i][j] && params.tiles[i][j] > -1 ? params.tiles[i][j] : null)

				this.tiles[i][j] =new Tile({
					sprite: sprite
					,isSolid: !globalHollows.contains(sprite)
					,row: i
					,col: j
				});
			}
		}

		if(params.flipMap) {
			Object.each(params.flipMap, function(row, rowid){
				Object.each(row, function(flip, tileid) {
					this.tiles[rowid][tileid].flip = flip;
				},this);
			},this);
		}

		if(params.bombableTiles) {
			Array.each(params.bombableTiles, function(td){
				this.tiles[td.row][td.col].postBombSprite = td.tile;
				this.tiles[td.row][td.col].bombItem = td.item;
			},this);
		}

		if(params.tintData)	this.setTintData(params.tintData);
		if(params.hollowTiles) {
			Array.each(params.hollowTiles, function(tile) {
				var ct = this.tiles[tile[0]][tile[1]] ;
				ct.isSolid = false;
			},this);
		}

		if(params.eventTiles) {
			Array.each(params.eventTiles, function(event) {
				var ct = this.tiles[event.row][event.col] ;
				ct.enter = event.event;
			},this);
		}

		if(params.enemies) this.enemyData = params.enemies;

		this.addEvent('enter', this.onEnter);
		this.addEvent('leave', this.onLeave);
	}
	,onEnter: function() {
		console.log('Entered room',this);
		env.player.currentRoom = this;
		$('background').setStyle('background', this.background ? this.background : '#fcd8a8');
		if(!this.initialized) {
			if(this.enemyData) Array.each(this.enemyData, function(enm){new enm(null,null,this);},this);
			this.initialized = true;
		}

		if (this.scriptedEvent) {
			new this.scriptedEvent(this);
		}

	}
	,onLeave: function() {
		console.log('leaving room',this);
		if(this.background) {
			$('background').setStyle('background', '#fcd8a8');
		}
		
	}
	,getTiles: function() {
		return this.tiles;
	}
	,getTile: function(row,col) {
		if(!this.tiles[row] || !this.tiles[row][col]) console.log(this.row, this.col, row, col);
		return this.tiles[row][col];
	}
	,setTintData: function(tintGroups) {
		Array.each(tintGroups, function (tintData) {
			if(tintData.wholeRoom) {
				tintData.rows = [];
				for(var i=0; i<this.roomHeight; i++) {
					tintData.rows.push(i);
				}
			}
			if(tintData.rows) {
				Array.each(tintData.rows, function(row) {
					Array.each(this.tiles[row], function(tile) {
						tile.tintFrom = tintData.tintFrom;
						tile.tintTo = tintData.tintTo;
					},this);
				},this);
			}
			if(tintData.tiles) {
				Array.each(tintData.tiles, function(tile) {
					var ct = this.tiles[tile[0]][tile[1]] ;
					ct.tintFrom = tintData.tintFrom;
					ct.tintTo = tintData.tintTo;
				},this);
			}
			
			
		},this);
	}
});

