var Tile = new Class({
	Implements: Events
	,sprite: null
	,flip: null
	,postBombSprite: null
	,bombItem: null
	,isSolid: true
	,tintFrom: null
	,tintTo: null
	,initialize: function(params) {
		this.sprite = params.sprite;
		if(typeof params.isSolid !== undefined) this.isSolid = params.isSolid;
		if(!this.sprite) this.isSolid=false;
		if(params.row) this.row = params.row;
		if(params.col) this.col = params.col;
		this.addEvent('enter', this.onEnter);
	}
	,onEnter: function() {
		if(this.enter) this.enter();
	}
	,bomb: function() {
		if(this.postBombSprite && this.bombItem == 'bomb') {
			this.sprite = this.postBombSprite;
			this.isSolid = false;
			paintRoom();
		}
	}
	,fire: function() {
		if(this.postBombSprite && this.bombItem == 'fire') {
			this.sprite = this.postBombSprite;
			this.isSolid = false;
			paintRoom();
		}
	}
	,touch: function() {
		if(this.postBombSprite && this.bombItem == 'touch') {
		console.log('touch fired');
			if(this.sprite == 53) {
				new Armos(this.col*TILESIZE, (this.row+4)*TILESIZE);
			}
			if(this.postBombSprite instanceof Object) {
				this.sprite = -1;
				new this.postBombSprite(this.col*TILESIZE, (this.row+4)*TILESIZE);
			}
			else {
				this.sprite = this.postBombSprite;
			}
			this.isSolid = false;
			
			paintRoom();
		}
	}
});


